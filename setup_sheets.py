def get_or_create_sheet(sh, title, headers):
    try:
        ws = sh.worksheet(title)
        print(f"  Sheet '{title}' already exists — clearing and repopulating...")
        ws.clear()
    except gspread.exceptions.WorksheetNotFound:
        ws = sh.add_worksheet(title=title, rows=2000, cols=50)
        print(f"  Created sheet '{title}'")
    ws.append_row(headers)
    return ws

